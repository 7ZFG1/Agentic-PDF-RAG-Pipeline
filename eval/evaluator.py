import re
from typing import List, Dict, Any
import os
import sys

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
sys.path.insert(0, ROOT)

from llm.llm import GeminiLLM 

class LLMJudge:
    def __init__(self, llm_client: GeminiLLM):
        """
        Evaluator class. Manages API calls via the externally injected 
        (dependency injection) llm_client.
        """
        self.llm = llm_client

    def build_judge_prompt(self, question: str, reference: str, prediction: str) -> str:
        """Creates the prompt required for the LLM to act as a judge."""
        return f"""You are an impartial and expert AI evaluator.
Your task is to measure the accuracy of the answer generated by the model to the given question, based on the reference answer (Ground Truth).

Evaluation Criteria:
- Does the generated answer contain the core information from the reference answer accurately and completely?
- Is there any misleading or incorrect information (hallucination) in the generated answer?

Question: {question}
Reference Answer: {reference}
Generated Answer: {prediction}

Please provide your evaluation strictly in the following format:
Explanation: [A short step-by-step explanation of why you gave this score]
Score: [1 if the answer is correct and captures the main idea, 0 if it is wrong or completely irrelevant]
"""

    def evaluate(self, question: str, reference: str, prediction: str) -> Dict[str, Any]:
        """Evaluates a single question-answer pair and parses the score."""
        prompt = self.build_judge_prompt(question, reference, prediction)
        
        # API call
        response_text = self.llm(prompt)

        # Extract the 'Score: 1' or 'Score: 0' format from the model using Regex
        score_match = re.search(r"Score:\s*([01])", response_text, re.IGNORECASE)
        score = int(score_match.group(1)) if score_match else 0

        return {
            "question": question,
            "prediction": prediction,
            "judge_response": response_text.strip(),
            "score": score
        }

    def run_evaluation(self, dataset: List[Dict[str, str]]) -> Dict[str, Any]:
        """Evaluates the entire given dataset and calculates overall accuracy."""
        results = []
        total_score = 0

        print(f"\n[Starting evaluation for {len(dataset)} samples...]")
        for i, data in enumerate(dataset):
            print(f"Evaluating ({i+1}/{len(dataset)})...")
            eval_result = self.evaluate(
                question=data["question"],
                reference=data["reference"],
                prediction=data["prediction"]
            )
            results.append(eval_result)
            total_score += eval_result["score"]

        # Calculate accuracy percentage
        accuracy = (total_score / len(dataset)) * 100

        return {
            "accuracy": accuracy,
            "total_samples": len(dataset),
            "correct_samples": total_score,
            "results": results
        }

